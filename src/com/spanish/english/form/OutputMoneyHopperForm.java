package com.spanish.english.form;

public class OutputMoneyHopperForm {
private long[] coins;
	
	
	private long[] notes;
	
	
	private long[] tokens;
	
	
	private long[] bills;


	public long[] getCoins() {
		return coins;
	}


	public void setCoins(long[] coins) {
		this.coins = coins;
	}


	public long[] getNotes() {
		return notes;
	}


	public void setNotes(long[] notes) {
		this.notes = notes;
	}


	public long[] getTokens() {
		return tokens;
	}


	public void setTokens(long[] tokens) {
		this.tokens = tokens;
	}


	public long[] getBills() {
		return bills;
	}


	public void setBills(long[] bills) {
		this.bills = bills;
	}

}
